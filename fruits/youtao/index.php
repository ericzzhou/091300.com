                            营养
                            油桃益处：富含维生素C，油桃由桃子改良栽培而成，比桃子甜，营养也丰富一些。一个新鲜油桃所含的维生素C，几乎可以满足成人一天所需。维生素C不仅有助于身体吸收铁和维护免疫系统，而且对合成皮肤的重要组成部分胶原至关重要。胶原还能促进伤口结疤，对伤口愈合十分重要。 油桃的皮比桃子光滑，没有讨厌的绒毛。果肉较硬，颜色有粉红、黄色、白色的，比桃子香味浓。油桃所含热量比桃子略多，平均为三十六大卡。
                            油桃营养丰富，含糖13%、有机酸1.5%、果胶1%蛋白质1.1%,每100克果肉含维生素C12毛身、磷40毛身、钙12毛身、铁0.8毛身、镁14.5毛身。此外还含有17种人体所需要的氨基酸类、胡萝卜素等。
                            油桃的主要成分是蔗糖，而维生素与矿物质的含量较少，但是属于纤维成分的果胶颇多，其有整肠的功用。将桃子做为汉方药使用是由于蓓蕾中含有的配糖体，对于利尿或便秘颇具效果。而桃子的叶郡，据说可用来沐浴并可去除痱子。[8]
                            食用功效
                            1、油桃有补益气血、养阴生津的作用，尤其适合大病之后，气血亏虚，面黄肌瘦，心悸气短者食用。[9]
                            2、油桃的含铁量较高，是缺铁性贫血病人的理想辅助食物，另外，油桃含钾多，含钠少，适合水肿病人食用。
                            3、桃仁提取物有抗凝血作用，并能抑制咳嗽中枢而止咳，同时能使血压下降，可用于高血压病人的辅助治疗。此外，桃仁还有活血化淤、润肠通便的作用，可用于闭经、跌打损伤等辅助治疗。
                            长相有几分像李子，有几分像桃子。多吃李子容易上火，吃油桃也会上火，俗话说，再美的东西吃多了都会有损害。所以说任何东西，吃得恰当才是最关键的。如果使用过多，就不仅仅是上火这么简单了，很有可能使身体有严重的不适反应。
                            1. 凡是内热偏盛、易生疮疖的人不宜多吃。多吃油桃容易上火，但是，吃果脯就没有这个弊端。
                            2. 李时珍曾说：“生桃多食，令人膨胀及生疮疖，有损无益。”油桃好吃，但不能多食。常言“桃养人”是指食桃要适度，恰到好处，但鲜桃不宜贮藏。
                            3. 桃仁虽然有破血行淤、滑肠通便的功效，但是桃仁含有挥发油和大量的脂肪油，泻多补少，所以不要多吃，桃仁吃多了，可以导致中毒，早期有恶心、呕吐、头疼、头晕、视力模糊、心跳加速等现象，严重者可导致心跳停止。尤其是孕妇，更不要食用。
                            油桃的营养丰富，如果能经常食用对身体是有好处的，但是吃多了就容易上火。
