                        <h2 id="-">物种分类</h2>
                        <div>
                            桃原产于中国西北黄土高原，中国拥有1000多个品种。油桃作为桃的一个变种，因其果面光滑无毛，很受广大消费者欢迎。甘肃河西的紫桃、新疆的黄肉李光桃是其典型代表。但油桃在中国作为栽培品种，用于生产中的时间较为短暂。<br/>
                            上世纪70年代，河南省农业科学院果树研究所、北京市农林科学院等科研单位先后从欧美、日本等地引入油桃品种进行试栽，但由于引入油桃大多为酸甜型品种，不符合中国群众的消费习惯而发展缓慢。<br/>
                            20世纪80年代以后，中国科研单位加快了甜油桃选育进程，先后推出了曙光、华光等，使中国的油桃品种跃上了一个新台阶。艳光、瑞光系列、秦光系列和中油系列等甜味品种，改变了中国油桃品种组成，并很快成为新兴果树之一。<br/>
                            <b>瑞光5号</b><br/>
                            瑞光5号，果实短椭圆形，纵径6.50厘米，横径6.40厘米，侧径6.40厘米。平均单果重145克，大果重158克。果顶圆，缝合线浅，两侧较对称，果形整齐。果皮底色黄白，果面着紫红或玫瑰红色点或晕，不易剥离。果肉白色，肉质细，硬溶质，味甜，风味较浓，黏核，鲜核重82克。含可溶性固形物7.4%～10.5%，可溶性糖7.0228%，可滴定酸0.3624%，维生素C7.682 8毫克100克。北京地区7月8 ～15日成熟。 树势强健，树冠较大，发枝力强。复花芽较多，占50%。果实7月上中旬采收，果实发育期85天左右。落叶期为10月下旬，年生育期210天左右。树姿半开张，一年生枝绿色，阳面红褐色。叶长椭圆披针形，叶面平展或略有皱，叶基楔形，叶尖渐尖，深绿色，具有光泽，蜜腺肾形，2～5个。花为铃形，雌蕊高于雄蕊，花粉多。该品种为优良的早熟油桃品种，果个大且圆整，风味甜，丰产。多雨年份有少量裂果。<br/>
                            北京、山东、河北有栽培，发展较快。<br/>
                            <b>瑞光7号</b><br/>
                            瑞光7号，北京市农林科学院林果所杂交育成，1989年命名，1997年通过审定。<br/>

                            果实近圆形，纵径5.75厘米，横径5.60厘米，侧径5.62厘米。平均单果重145克，大果重183克。果顶圆，缝合线浅，两侧对称，果形整齐。果皮底色淡绿或黄白，果面1/2至全面着紫红或玫瑰红色点或晕，不易剥离。果肉黄白色，肉质细，硬溶质，耐运输，味甜或酸甜适中，风味浓，半离核或离核，鲜核重8.0克。含可溶性固形物9.5%～11.0%，可溶性糖8.0652%，可滴定酸0.5806%，维生素C9.8600毫克/100克。北京地区7月13～20日成熟。树势中等，树冠较小。复花芽较多，占60%。<br/>
                            花芽起始节位1～2节。各类果枝均能结果，丰产性好，盛果期树亩产可达2000千克以上。北京地区4月初萌芽，4月16～23日盛花，花期1周。果实7月中旬采收，果实发育期90天左右。落叶期为10月下旬，年生育期210天左右。树姿半开张，一年生枝绿色，阳面红褐色。叶长椭圆披针形，叶面平展，叶基楔形，叶尖渐尖，深绿色，具光泽，蜜腺肾形，2～5个。花为蔷薇形，雌蕊略高于雄蕊，花粉多。该品种为优良的早中熟油桃品种，果个大，果面红，肉质硬，风味浓，丰产。不足之处是果面光泽度不够。<br/>
                            北京、河北、辽宁等地有栽培。<br/>
