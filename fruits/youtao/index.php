                        <h2 id="-">生长环境</h2>
                        <div>
                            油桃是普通桃的变种，主产区以山东、河北等北方地区为主，[3]  当地称为"李光桃"，它对土壤、气候的适应性和栽培技术跟普通桃基本一样。基于过往错误的认识，“桃驳李”被认为是一种把桃嫁接至同属的李树后所结的果子，又或者是桃和李的杂交种，所以外表像李子一样光滑，但味道和口感都跟桃子相似。<br/>
                            油桃由于果面光滑无毛，果皮韧性较差，大多数品种在南方高温多湿的气候条件下，易裂果和产生日灼。在南方一般要选择短低温早熟和特早熟品种，最好选择通过南方生产实践不裂果或裂果较轻的品种进行栽培。另外，由于南方地区6～8月温度高、湿度大，病虫害特别严重，尤其是螨类虫害最盛，所以，最好选择7月份以前成熟的早熟和特早熟品种为宜。
                        </div>

