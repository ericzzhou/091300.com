                        <h2 id="-">口感</h2>
                        <div>
                            香、甜、脆一应俱全。油桃风味浓甜（含糖极高），十分适合中国人喜甜的饮食习惯；香味浓郁，清香可口；肉质细脆，爽口异常，甜而不腻。<br/>
                            油桃的肉质是脆的，水分相对少些；普通的桃子生的时候是脆的，熟了就是软的，水分很多。<br/>
