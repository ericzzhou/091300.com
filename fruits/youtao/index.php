                        <h2 id="-">储藏及运输</h2>
                        <div>
                            储藏
                            家庭中常见的容器有缸、罐、坛、纸箱、木箱，这些容器都可用来贮藏油桃。所用的缸、罐、坛必须洗净擦干，并用白酒涂擦缸、罐、坛的内壁，也可在其中放半瓶白酒（用量可根据贮量的多少而定），瓶口敞开。
                            油桃采收后先放在阴凉处摊放几天，然后分层放在缸、罐或坛内。
                            装好后再喷洒上白酒，根据贮量不同可喷洒50—150克不等，用棉絮盖其上再蒙上一层塑料布封口，防止酒气散发，吃油桃时随取随盖，一般可贮藏半年以上。 　用于贮藏油桃的箱子要清洁无味，箱底和四周放两层纸，每个油桃也用柔软的白纸包好，整齐地摆在箱子里。
                            运输
                            通风隔热车
                            隔热车是一种仅有隔热功能的车体，车内无任何制冷和加温设备。在货物运输的过程中，主要依靠隔热性能良好的车体的保温作用来减少车内外的热交换，以保证货物在运输期间温度的波动不超过允许的范围。这种车辆具有投资少、耗能少和节省经费等优点。用加冰冷藏车改造的通风隔热车运输苹果比较经济实惠，在车内加冰等辅助措施可提高运输效果。
                            冷藏车
                            铁路冷藏运输，运用冷藏、保温、防寒、加温、通风等方法，在铁路上快速优质地运输。冷藏车的特点是：车体隔热，密封性好，车内有冷却装置，在温热季节能在车内保持比外界气温低的温度。冷藏车在寒季还可以用于不加冷保温的运送或加温运送，在车内保持比外界气温高的温度。
                            集装箱
                            集装箱运输（containerized traffic）是当今世界正在发展的运输工具，既省人力、时间，又保证产品质量，实现“门对门”的服务，是现代运输工具中的一大革新。
                            在集装箱的基础上增加箱体隔热层、制冷及加温设备，即为冷藏集装箱，它可以维持新鲜苹果的温度。在冷藏集装箱的基础上，加设气密层，改变箱内气体成分（降低O2浓度和增加CO2浓度），即为冷藏气调集装箱。控制气体成分的方法，可在车站、码头用气调机调节箱内气体，或是在箱内装液氮罐，释放氮代替箱内空气，达到降氧的目的。冷藏气调集装箱较之冷藏集装箱更能保持货品新鲜品质。
