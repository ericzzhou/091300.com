                        <h2 id="-">形态特征</h2>
                        <div>
                            油桃（学名：nectarine; nectarines）：蔷薇科、桃属植物。落叶小乔木；叶为窄椭圆形至披针形
                            ，长15厘米，宽4厘米，先端成长而细的尖端，边缘有细齿，暗绿色有光泽，叶基具有蜜腺；树皮暗灰色，随年龄增长出现裂缝；花单生，从淡至深粉红或红色，有时为白色，有短柄，直径4厘米，早春开花；近球形核果，表面有毛茸，肉质可食，为橙黄色泛红色，直径7.5厘米，有带深麻点和沟纹的核，内含白色种子。<br/>
                            油桃的核在成熟后易分离，种子一般不能发芽。桃李等水果的核一般含有极少量的氰类物质，稍有毒，但不足以致发不良反应，并无大碍。<br/>
                            花单生，先于叶开放，直径2.5-3.5厘米；花梗极短或几无梗；萼筒钟形，被短柔毛，稀几无毛，绿色而具红色斑点；萼片卵形至长圆形，顶端圆钝，外被短柔毛；花瓣长圆状椭圆形至宽倒卵形，粉红色，罕为白色；雄蕊约20-30，花药绯红色；花柱几与雄蕊等长或稍短；子房被短柔毛。<br/>
                            叶片长圆披针形、椭圆披针形或倒卵状披针形，长7-15厘米，宽2-3.5厘米，先端渐尖，基部宽楔形，上面无毛，下面在脉腋间具少数短柔毛或无毛，叶边具细锯齿或粗锯齿，齿端具腺体或无腺体；叶柄粗壮，长1-2厘米，常具1至数枚腺体，有时无腺体。<br/>
                            油桃表皮是无毛而光滑的、发亮的、颜色比较鲜艳，好象涂了一层油；普通的桃子表皮有绒毛，颜色发
                            红或微黄，无亮光。<br/>
                            其它任何桃的表面都是有毛的，但是油桃因其表面光滑如油、无毛，与苹果、李子的表面一样光滑故称其为油桃。其它桃的果面只有部分呈红色，但油桃的整个果面都呈鲜红色，跟樱桃一样，因此油桃以其果皮无毛、果色鲜红迷人、光彩夺目的外观深受市场的欢迎。它是国际市场上风行的一种新兴水果，专家预测油桃将逐渐取代其它桃而一统天下，二十一世纪是油桃的时代。<br/>
                            <b>优良性状</b>
                            1、表面光丽迷人。其它任何桃的表面都是有毛的，但是油桃因其表面光滑如油、无毛，与苹果、李子的表面一样光滑故称其为油桃。其它桃的果面只有部分呈红色，但油桃的整个果面都呈鲜红色，跟樱桃一样，因此油桃以其果皮无毛、果色鲜红迷人、光彩夺目的外观深受市场的欢迎。它是目前国际市场上风行的一种新兴水果，专家预测油桃将逐渐取代其它桃而一统天下，二十一世纪是油桃的时代。<br/>
                            2、风味特佳，香、甜、脆一应俱全。油桃风味浓甜（含糖极高），十分适合中国人喜甜的饮食习惯；香味浓郁，清香可口；肉质细脆，爽口异常。<br/>
                            3、耐贮运。油桃的部分品种成熟后果实脆硬，极耐长途运输，其耐贮运性能大大超过水蜜桃。
