                        <h2 id="-">推荐做法</h2>
                        <div>
                            <b>西瓜皮酱</b><br/>
                            原料配方<br/>
                            西瓜皮500克 白糖500克 柠檬酸3克 冷水500毫升 明胶8克
                            <br/><b>制作方法</b>
                            <br/>1.将西瓜皮洗净，切成碎块，用组织捣碎机搅成泥状（也可用刀切碎）。
                            <br/>2.取锅，将捣碎的西瓜皮、柠檬酸和水一同倒入锅内，略搅拌几次，用旺火煮沸约5分钟，再加入白糖，用力搅拌，待白糖溶化后改用小火煮约10分钟，此时必须经常搅动锅内溶液，避免糊锅。
                            <br/>3.用少许水浸泡明胶，并上火加热。当明胶充分溶于水之后，将明胶溶液倒入果酱锅内，搅拌均匀，再煮10分钟。然后用汤匙取少量西瓜皮酱倒在平盘内，如果无流散现象，说明果酱已煮得恰到好处，即可关火。
                            <br/>4.将西瓜皮酱倒入经消毒的容器内，加盖密封，置阴凉通风处保存。
                            <br/><b>西瓜皮醋</b><br/>
                            1.配料<br/>
                            每50kg西瓜皮加曲6kg，高梁糠10kg（谷糠也行），食盐4kg，可以制醋50kg。<br/>
                            <br/>2.制法 <br/>
                            先把西瓜皮洗净，煮熟，晾凉装罐加曲捣烂拌匀，发酵5天后拌糠，每天早晚搅拌一次，过4-5天加盐，6天出坯，然后把1/3的坯装入熏罐烤熏4天，再把2/3的坯加生水过滤熬开，添入熏坯，过滤即成美味酸醋。为使醋变香和不变质，可按0.5kg醋加2-3滴酒精的比例，把酒清滴在醋里，或者滴2-3滴普通烧酒。
