                        <h2 id="-">民俗文化</h2>
                        <div>
                            <b>历史传说</b>
                            <br/>
                            提起“大荔西瓜”无人不知，无人不晓。在《大荔县志》中所记“西瓜出沙苑，味甘美，甲于秦中。”1981年后，大荔县加强西瓜的技术指导，培训技术力量，进行品种更新，先后引进新澄杂交一代、早花、湘蜜、中育等27年良种。并采塑料薄膜进行地面覆盖，把成熟期提前一个多月。
                            1984年又引进新红宝、优红、郑杂等品种，面积发展到10多万亩，种植地区由洛南发展到洛惠灌区。如今更是品种繁多，品种有"绿宝八号"、"西农8号"、"新红宝868"、"早冠龙"、"卡奇"等大果型有籽西瓜和"黑密2号"、"黑骏马"、"黑元帅"等中果型无籽西瓜达20多个品种，引进"小兰"、"黑美人"、"金美人"、"早春红玉"等小果型早熟礼品西瓜10多个品种。在栽培技术上由部分地膜覆盖发展为全部覆盖，部分面积还采用地膜和拱棚双层覆盖。其中沿洛、渭河地区的羌白、冯村、石槽、韦林等乡镇种植西瓜3.6万亩,北部塬区和黄河滩区的两宜、高明、范家、平民、赵渡、朝邑6个乡镇种植西瓜6万余亩。
                            <br/>
                            <b>当地文化</b>
                            <br/>
                            大荔西瓜，更与大荔人的勤苦割不断。清明前后，耕田下种，他们三五家联合，互帮互助，挖土的、撒种的、培土的各自分工，辛苦劳作。端午左右，压蔓培土，他们提水带饭，一呆便是一整天。入暑时节，担心瓜秧受旱，细心的农人头顶烈日，挑水浇瓜，一窝瓜，一瓢水，直到瓜长至笼口大，结果瓜红了，他们晒黑了，瓜圆了，他们累瘦了。当他们弯着腰撒下一粒粒瓜种时，当他们坐在地畔，啃着干硬的馒头时，当他们脱去上衣，摸着那被扁担磨肿压疼的肩头时，他们何曾叫过苦，何曾喊过累，可当他们看着满园的西瓜长圆长大以至装车卖走时，他们却是何等的开心，他们脸上犁沟似的皱纹准会绽成朵花。
                            大荔西瓜，是大荔人勤苦的象征，大荔西瓜，是大荔人用汗水哺育的结果。大荔人以大荔西瓜而自豪，大荔西瓜更以大荔人而骄傲。大荔瓜香人更香，大荔瓜美人更美。
                            大荔西瓜，不仅色香，而且味甜。一牙瓜，远看一轮红月亮，近观一艘小船儿，红瓤嵌着黑子儿，煞是诱人。瓜瓤由米粒大的瓜肉组成，一粒粒堆积起来，密而含汁，疏而不漏。瓜子周围，皆白蒙蒙的，像裹了道轻纱，更像洒上了白砂糖，太阳光一照，瓤儿红得耀眼，籽儿黑得发亮，瓤汁间的汁水，也一闪一闪的，惹得人禁不住伸手便拿，张口就咬，嚼几下，满口的汁水直溢到唇角，甜得你灌了蜂蜜一般。一股脑咽下，那汁水便渗透你每一眼毛孔，染得你周身尽甜丝丝了。这时候，什么橙汁桔粉白糖水的味儿，全没了生气，你真是沉浸于这源于自然的甜蜜中了。真应了那句话‘红沙瓤、晒冰糖、吃一口、流一手’。
                            大荔西瓜，因何如此大，如此香，又如此甜呢？这与大荔的气候水土是分不开的。大荔地处关中平原东部、黄土高原南缘，典型的温带半干旱气候，为西瓜的生长提供了独特的天然条件。其北依镰山，南傍渭水，黄河临东，洛水贯中，三河交汇的黄绵土质又为西瓜的丰产创造了优越的地理环境。加之素有农业大县美誉的大荔人长期以来摸索试验，形成了一套堪称专利的务瓜经验，造就了大荔西瓜的声名远扬。或许大荔西瓜仅适于这方水土！不然，外地人学了却为何务不出这么撩人心弦的西瓜呢？
