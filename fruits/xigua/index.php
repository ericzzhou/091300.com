                        <h2 id="-">特点</h2>
                        <div>
                            大荔西瓜，个头儿大。大如笸箩小，小若牛头大，
                            粗细像盆口，长短似枕头，竖起来，能和小娃比高低，平放下，能与竹笼赛大小，十五斤起码，二十斤一般，瓜王总在四五十斤。当地人见怪不怪，种瓜者老嫌瓜小，岂不知，就连最小的，外地人见了也直瞪眼，问一问，这还是不是西瓜，呦，这真是个大冬瓜！他们惊奇，惊奇这方水土，怎产得如此大样的西瓜？惊奇这里的农人，男女皆手大脚粗，面色如酱，却育得这般赢人的西瓜？他们百思不得其解，末了仅一句话，大荔西瓜，真了不得！
                            大荔西瓜，香气宜人。绿皮红瓤黑子儿，想来也羡得人涎水直流。抱来一个大西瓜放桌上，未及动刀，桌旁准满是人。孩子们像虔诚的教徒一个个蹲在桌边，胳膊搭桌沿，眼珠子盯着刀口；中年人坐着小凳，看似平静内心早急得慌；老年人不蹲也不坐，却背了手在旁边来回转悠，他们是等着听那咔嚓的声响哩。圆嘟嘟的西瓜绿荫荫的皮，敲一下，砰砰中带着回音，像胡基蛋儿掉进了水井里。不用问，十二分地保熟，刀尖触及，哗地裂作两半，当红瓤露出时，那香气，便溢得满屋子都是。
                            大荔西瓜堪称瓜中之王，富含维生素A、Bl、B2、C，葡萄糖、蔗糖、果糖、苹果酸、谷氨酸和精氨酸等，有清热解暑、利小便、降血压的功效，对高热口渴、暑热多汗、肾炎尿少、高血压等有一定的辅助疗效。
                            大荔西瓜以瓤沙、味甜、水多、营养丰富而闻名。成熟果实除含有大量水分外，瓤肉含糖量一般为5～12%，包括葡萄糖、果糖和蔗糖。甜度随成熟后期蔗糖的增加而增加。不含淀粉，采后贮藏期间甜度会因双糖水解为单糖而降低。瓜子可作茶食，瓜皮可加工制成西瓜酱。在中医学上以瓜汁和瓜皮入药，功能清暑解热。西瓜是夏天的典型水果，也是夏季的主要水果，在炽热的夏日或气温闷热的热带夜晚，只要有冷冻的西瓜，便具有消除暑热的效果。

