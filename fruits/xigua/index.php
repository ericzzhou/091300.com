                        <h2 id="-">地理分布</h2>
                        <div>
                            大荔西瓜产地位于中国陕西北部黄土高原水果之乡—陕西大荔县是我国唯一具备西瓜生产七项气象指标的地区，其所处的纬度，海拔高度，土壤厚度特别适宜西瓜生产，并且没有工业污染，被农业部确定为中国水果优势产业带．有多年的西瓜管理经验和大面积的新优品种栽植。
