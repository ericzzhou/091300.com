                        <h2 id="-">形态特征</h2>
                        <div>
                            一年生匍匐或攀援草本；茎、枝有棱，有黄褐色或白色的糙硬毛和疣状突起。卷须纤细，单一，被微
                            柔毛。叶柄长8-12厘米，具槽沟及短刚毛；叶片厚纸质，近圆形或肾形，长、宽均8-15厘米，上面粗糙，被白色糙硬毛，背面沿脉密被糙硬毛，边缘不分裂或3-7浅裂，裂片先端圆钝，有锯齿，基部截形或具半圆形的弯缺，具掌状脉。<br/>
                            花单性，雌雄同株。雄花：数朵簇生于叶腋；花梗纤细，长0.5-2厘米，被柔毛；花萼筒狭钟形，密被白色长柔毛，长6-8毫米，裂片近钻形，直立或开展，比筒部短；花冠黄色，长2厘米，裂片卵状长圆形，急尖；雄蕊3毫米，花丝极短，药室折曲，药隔顶端引长；退化雌蕊长约1毫米。雌花：单生，花梗粗糙，被柔毛；子房长椭圆形，密被长柔毛和长糙硬毛，花柱长1-2毫米，柱头靠合，长约2毫米。<br/>
                            果实的形状、颜色因品种而异，通常为球形或长椭圆形，果皮平滑，有纵沟纹，或斑纹，无刺状突起，果肉白色、黄色或绿色，有香甜味；种子污白色或黄白色，卵形或长圆形，先端尖，基部钝，表面光滑，无边缘。花果期夏季。<br/>
                            哈密瓜分网纹皮、光皮两种。按成熟期分为早熟瓜、夏瓜(中熟)、冬瓜(晚熟)等品种群。不同品种的瓜，其形态、颜色、皮纹也不一样。常见的优良品种有红心脆、黑眉毛蜜极甘(瓜皮有深色条纹如秀眉，故称黑眉毛；“蜜极甘”，维吾尔语意为花裙子)、炮台红、铁皮、青麻皮、网纹香梨、哈密加格达、小青皮、白皮
                            。
