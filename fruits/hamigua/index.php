                        <h2 id="-">主要价值</h2>
                        <div>
                            营养价值
                            哈密瓜不但香甜，而且富有营养价值。据分析，哈密瓜的干物质中，

                            含有4.6%-15.8%的糖分，纤维素2.6%-6.7%，
                            还有苹果酸、果胶物质、维生素A、B、C，尼克酸以及钙、磷、铁等元素。其中铁的含量比鸡肉多两三倍，比牛奶高17倍。新疆人很爱食哈密瓜，认为多吃瓜可以祛病延年，这一说法不无道理。
                            哈密瓜除供鲜食，还可制作瓜干、瓜脯、瓜汁。哈密瓜削皮可以用果蔬削皮机中的大果削皮机适用于食品规模加工。瓜蒂瓜籽可入药治病，瓜皮喂羊能促肥增膘。
                            哈密瓜籽可用于做精油，富含药用、美容、保健、生理活性的哈密瓜种子，采取纯物理、冷榨方式精制而成。其色泽金黄，滋味清香，还有令人愉快的水果滋味；充分保留了哈密瓜籽中独有的药用、美容、保健营养成分不受破坏。具有超强的抗氧能力，实属

                            上等纯天然药用、美容、保健、营养精油，可广泛应用在医药、美容、保健、营养食品等领域。
                            在每一百克瓜肉中还有蛋白质0.4克，脂肪0.3克，灰分元素2克。其中钙14毫克，磷10毫克，铁1毫克。请不要小看这1毫克铁，它对人体的造血功能和发育有很大关系。
                            哈密瓜肉中维生素的含量比其它水果也毫不逊色。在哈密瓜鲜瓜肉中，维生素的含量比西瓜多4至7倍，比苹果高6倍，比杏子也高1.3倍。这些成分，有利于人的心脏和肝脏工作以及肠道系统的活动，促进内分泌和造血机
                            能，加强消化过程。
                            哈密瓜有“瓜中之王”的美称，含糖量高，形态各异，风味独特，有的带奶油味，有的含柠檬香，但都味甘如蜜，奇香袭人，饮誉国内外。在诸多哈密瓜品种中，以“红心脆”、“黄金龙”品质最佳。哈密瓜不仅好吃，而且营养丰富，药用价值高。
                            食疗作用
                            中医认为，甜瓜类的果品性质偏寒，还具有疗饥、利便、益气、清肺热止咳的功效，适宜于肾病、胃病、咳嗽痰喘、贫血和便秘患者。
                            哈密瓜虽然不是夏天消暑的水果，但是能够有效防止人被晒出斑来。夏日紫外线能透过表皮袭击真皮层，令皮肤中的骨胶原和弹性蛋白受到重创，这样长期下去皮肤就会出现松弛、皱纹、微血管浮现等问题。
                            同时导致黑色素沉积和新的黑色素形成，使皮肤变黑、缺乏光泽，造成难以消除的太阳斑。
                            而哈密瓜中含有丰富的抗氧化剂，而这种抗氧化剂能够有效增强细胞抗防晒的能力，减少皮肤黑色素的形成。另外，每天吃半个哈密瓜可以补充水溶性维生素C和B族维生素，能确保机体保持正常新陈代谢的需要。
                            哈密瓜可以很好的预防一些疾病，哈密瓜中钾的含量是最高的。钾对身体是非常有益的，钾可以给身体提供保护，还能够保持正常的心率和血压，可以有效的预防冠心病，同时，钾能够防止肌肉痉挛，让人的身体尽快的从损伤中恢复过来。
                            美容防晒
                            美味可口的哈密瓜不仅仅是人们夏季消暑的上佳选择，以新疆哈密淖毛

                            湖出产的品质最佳。而且它还有着特殊的作用。法国的一位农夫在无意中发 现，有种哈密瓜对人们的皮肤特别有好处，它能防止人们娇嫩的皮肤被太阳晒伤。
                            据《每日邮报》日前报道，这位农夫在无意中发现，一种特殊种类的哈密瓜和其他瓜相比，能够储藏更长的时间而不会坏掉。当法国巴黎大学的科学家对这种瓜进行深入研究时，他们发现，这种瓜富含抗氧化物质，能够有助于抵抗可对细胞造成损害的氧自由基。而过度暴露在太阳下则可触发氧自由基的生成。
                            这种哈密瓜的瓜汁已经被浓缩后，冻干成为了药片制剂。除了擦防晒霜之外，人们还可以通过每日服用这种片剂来防止皮肤被太阳晒伤。巴黎大学的有关专家对此表示：“这种瓜汁精华能够给你提供一种额外的保护，这是你只吃一个哈密瓜所不能得到的。”
                            注意事项
                            搬动哈密瓜应轻拿轻放，不要碰伤瓜皮。受伤后的瓜很容易变质腐烂，不能储藏。 哈密瓜性凉，不宜吃得过多，以免引起腹泻。
                            　　哈密瓜含糖较多，糖尿病人应慎食。哈密瓜多为椭圆形或橄榄形。它的颜色为果
                            绿色带网纹的， 金黄色的，花青色的等等。 用鼻子去闻瓜，一般有香味的，成熟度适中。没有香味或香味淡泊的，是成熟度较差的，可以放些时候再吃。
                            挑瓜时可以用手摸一摸，如果瓜身坚实微软，成熟度就比较适中。如果太硬则不太熟，太软就是成熟过度。瓜瓤为浅绿色的，吃时发脆，如是金黄色的，吃上去发粘，白色柔软多汁。总之，哈密瓜的种类很多，希望您挑选时注意这几点。
                            哈密瓜性凉不宜吃的过多，以免引起腹泻。
                            患有脚气病、黄疸、腹胀、便溏、寒性咳喘以及产后、病后的人不宜多食；所以孕妇可以在怀孕的时候适当的吃点哈密瓜，在产后就不要吃了哈。因为哈密瓜是偏凉的。要是虚寒体质的也最好不要吃。
